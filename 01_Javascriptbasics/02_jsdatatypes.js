"use strict"; // Treat all JavaScript code as the latest version

// Basic addition example
console.log(3 + 3); // Prints the sum of 3 and 3

console.log("Mahin"); // Prints a string

// Declaring variable with different data types
let name = "Mahin"; // A string to store name
let age = 18; // A number to store age
let isLoggedIn = false; // Boolean to check login status
let state; // A variable declared but not initialized (undefined)

// Different data types in JavaScript:
// - Number: Numeric values like 2, 3.14
// - BigInt: Large integers beyond the safe limit of Number
// - String: Text values like "Mahin"
// - Boolean: Logical values (true or false)
// - Null: An empty or non-existent value
// - Undefined: A variable declared but not assigned a value
// - Symbol: A unique identifier

// Objects: Used to store multiple related values as key-value pairs

// Type checking examples
console.log(typeof undefined); // undefined
console.log(typeof null); // object (a quirk in JavaScript)
